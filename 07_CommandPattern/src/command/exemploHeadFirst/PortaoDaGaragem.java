public class PortaoDaGaragem {
	
	public void abrir() {
		System.out.println("abrindo portao...");
	}

	public void fechar() {
		System.out.println("Fechando portao...");
	}
	
	public void parar() {
		System.out.println("Parando o portao...");
	}
	
	public void ligaLuz() {
		System.out.println("Acendendo luz da garagem...");
	}
	
	public void desligaLuz() {
		System.out.println("Desligando luz da garagem...");
	}
	
}
