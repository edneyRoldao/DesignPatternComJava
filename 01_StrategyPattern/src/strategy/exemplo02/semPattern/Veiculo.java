package strategy.exemplo02.semPattern;

public abstract class Veiculo {

	//Atributos
	private String nome;
	private String marca;

	
	//Getters and Setters
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getMarca() {
		return marca;
	}

	public void setMarca(String marca) {
		this.marca = marca;
	}

}
